	public void verifyAndUpdatePassword() {
		uname = txt_uname.getText();
		contact = txt_contact.getText();
		newPasswd = String.valueOf(txt_passwd.getPassword());
		d_otp = txt_otp.getText();
		SQL = "SELECT * FROM users WHERE uname = '" + uname + "' AND contact = '" + contact + "'";

		if (!otp.equals(d_otp)) {
			JOptionPane.showMessageDialog(this, "Invalid OTP");
			txt_otp.requestFocus();
			return;
		}

		try {
			smt = con.createStatement();
			rst = smt.executeQuery(SQL);
			if (rst.next()) {
				SQL = "UPDATE users SET password = '" + newPasswd + "' WHERE uname = '" + uname + "'";
				int rst = smt.executeUpdate(SQL);
				if (rst == 1) {
					JOptionPane.showMessageDialog(this, "Password Updated");
					Login login = new Login();
					this.dispose();
					login.setVisible(true);
				}
			} else {
				JOptionPane.showMessageDialog(this, "Password Not Updated");
			}
		} catch (SQLException | HeadlessException e) {
			System.err.println(e);
		}
	}


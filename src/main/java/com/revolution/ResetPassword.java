    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
		SQL = "SELECT uname, contact FROM users WHERE uname = '" + txt_uname.getText() + "'";
		try {
			smt = con.createStatement();
			rst = smt.executeQuery(SQL);
			if(rst.next()) {
				if(rst.getString("contact").equals(txt_contact.getText())) {
					otp = new NotifyOTP().generateOTP(txt_contact.getText());
				} else {
					JOptionPane.showMessageDialog(this, "Please enter correct contact");
					txt_contact.requestFocus();
				}
			} else {
				JOptionPane.showMessageDialog(this, "Username not found", "Error", 2);
				txt_uname.requestFocus();
			}
		} catch(SQLException e) {
			System.err.print(e);
		}
    }//GEN-LAST:event_jButton2ActionPerformed


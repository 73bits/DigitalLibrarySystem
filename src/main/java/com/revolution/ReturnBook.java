	public void getIssueBookDetails() {
		bookId = txt_bookId.getText();
		memberId = txt_memberId.getText();
		SQL = "SELECT * FROM issue_book_details WHERE book_id = '" + bookId + "' AND member_id = '" + memberId + "' AND status = 'pending'";

		try {

			pst = con.prepareStatement(SQL);
			rs = pst.executeQuery();
			if (rs.next()) {
				lbl_issueId.setText(rs.getString("id"));
				lbl_bookName.setText(rs.getString("book_name"));
				lbl_memberName.setText(rs.getString("member_name"));
				lbl_issueDate.setText(rs.getString("issue_date"));
				lbl_dueDate.setText(rs.getString("due_date"));
				jPanel1.setBackground(new Color(0, 51, 153));
				lbl_error.setText("RECORD FOUND");
				fineDefaulters();
			} else {
				lbl_issueId.setText(dots);
				lbl_bookName.setText(dots);
				lbl_memberName.setText(dots);
				lbl_issueDate.setText(dots);
				lbl_dueDate.setText(dots);
				jPanel1.setBackground(Color.RED);
				lbl_error.setText("NO RECORD FOUND");

			}
		} catch (SQLException e) {
			System.err.println(e);
		}
	}

	public void fineDefaulters() {
		today = new Date();
		bookId = txt_bookId.getText();
		memberId = txt_memberId.getText();
		SQL = "SELECT * FROM issue_book_details WHERE due_date < '" + today
				+ "' AND book_id = " + bookId + " AND member_id = " + memberId + " AND status = 'pending'";
		try {
			smt = con.createStatement();
			rs = smt.executeQuery(SQL);
			if (rs.next()) {
				duedate = rs.getDate("due_date");
				if (duedate.compareTo(today) < 0) {
					long diff = today.getTime() - duedate.getTime();
					long late = TimeUnit.DAYS.convert(diff, TimeUnit.MILLISECONDS);
					jPanel1.setBackground(Color.RED);
					lbl_error.setText("PENALTY : " + late + " DAYS LATE || LATE FINE : Rs." + late * 5 + "/-");
				}
			}
		} catch (SQLException e) {
			System.err.println(e);
		}
	}

	public boolean returnBook() {
		boolean isReturned = true;
		bookId = txt_bookId.getText();
		memberId = txt_memberId.getText();
		SQL = "UPDATE issue_book_details SET STATUS = 'returned' WHERE book_id = " + bookId + " AND member_id = " + memberId + " AND status = 'pending'";

		try {
			pst = con.prepareStatement(SQL);
			int rowCount = pst.executeUpdate();
			if (rowCount <= 0) {
				isReturned = false;
			}
		} catch (SQLException e) {
			System.err.println(e);
		}
		return isReturned;
	}

	public void updateBookCount() {
		bookId = txt_bookId.getText();
		try {
			SQL = "UPDATE book_details SET quantity = quantity + 1 WHERE book_id = " + bookId + "";
			pst = con.prepareStatement(SQL);
			int rowCount = pst.executeUpdate();
			if (rowCount > 0) {
				JOptionPane.showMessageDialog(this, "BOOK COUNT UPDATED");
			} else {
				JOptionPane.showMessageDialog(this, "CAN'T UPDATE BOOK COUNT");
			}
		} catch (HeadlessException | SQLException e) {
			System.err.println(e);
		}
	}

